//arithmetic operators  
void main() {  

     int a = 2;  
     int b = 3; 

    // Adding a and b 
    print("Sum  (a + b) = ${a+b}"); 
 
    // Subtracting a and b 
    print("Difference (a - b) = ${a-b}"); 

    // Using unary minus
    var c = -a;
    print("Negation -(a) = $c");
 
    // Multiplication of a and b 
    print("Product (a * b) = ${a * b}"); 
 
    // Division of a and b  
    print("Division (b / a) = ${b / a}"); 
 
    // Using ~/ for integer division 
    print("Quotient (b ~/ a) = ${ b ~/ a}"); 
 
    //Remainder of b divides a 
    print("Remainder (b % a) = ${b % a}"); 
  

} 