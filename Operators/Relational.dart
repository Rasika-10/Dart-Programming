// Relational Operators 

void main() { 

        int a = 2; 
        int b = 3; 

        // Greater than 
        print("a is greater than b (a > b) : ${a > b}"); 

        // Less than 
        print("a is smaller than b (a < b) : ${a < b}"); 

        // Greater than or equal to 
        print("a is greater than or equal to b (a >= b) : ${a >= b}"); 

        // Less than or equal to 
        print("a is smaller than or equal to b (a <= b) : ${a <= b}"); 

        // Equality 
        print("a and b are equal (a == b) : ${a == b}"); 

        // Inequality 
        print("a and b are not equal (a != b) : ${a != b}"); 

} 